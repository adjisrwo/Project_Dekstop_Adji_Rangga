    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int idSiswa = studentId;
        String selectedBentukPelanggaran = (String) jComboBox1.getSelectedItem();

        // Retrieve the corresponding Pelanggaran object from the database
        Pelanggaran selectedPelanggaran = dbManager.getPelanggaranByBentuk(selectedBentukPelanggaran);
        if (selectedPelanggaran != null) {
            int idPelanggaran = selectedPelanggaran.getId();
            // Call the method to store the catatan pelanggaran
            dbManager.storeCatatanPelanggaran(idSiswa, idPelanggaran);
        } else {
            System.out.println("Error: Pelanggaran not found for bentuk " + selectedBentukPelanggaran);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

