    private void populateData() {
        // Populate Nama Siswa
        String namaSiswa = dbManager.getStudentName(studentId);
        jTextField3.setText(namaSiswa);
        
        jComboBox1.removeAllItems();

        // Populate Bentuk Pelanggaran dropdown
        List<Pelanggaran> pelanggaranList = dbManager.getAllPelanggaran();
        for (Pelanggaran pelanggaran : pelanggaranList) {
            jComboBox1.addItem(pelanggaran.getBentukPelanggaran());
        }
    }

    private void updateKategoriAndPoinFields() {
        String selectedBentukPelanggaran = (String) jComboBox1.getSelectedItem();
        List<String> kategoriList = dbManager.getKategoriByBentukPelanggaran(selectedBentukPelanggaran);

        if (!kategoriList.isEmpty()) {
            String selectedKategori = kategoriList.get(0); // Assuming only one kategori is returned
            int poin = dbManager.getPoin(selectedBentukPelanggaran, selectedKategori);

            jTextField5.setText(selectedKategori);
            jTextField1.setText(String.valueOf(poin));
        } else {
            jTextField5.setText("");
            jTextField1.setText("");
        }
    }




