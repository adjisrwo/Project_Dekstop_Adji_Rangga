    private void pelanggaranData() {
        List<Pelanggaran> pelanggaranList = dbManager.getAllPelanggaran();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        for (Pelanggaran pelanggaran : pelanggaranList) {
            Object[] row = new Object[4];
            row[0] = pelanggaran.getId();
            row[1] = pelanggaran.getBentukPelanggaran();
            row[2] = pelanggaran.getKategori();
            row[3] = pelanggaran.getPoin();
            model.addRow(row);
        }
    }
    
