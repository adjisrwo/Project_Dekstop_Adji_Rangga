    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String bentukPelanggaran = jTextField3.getText();
    String kategori = jTextField4.getText();
    int poin = Integer.parseInt(jTextField1.getText());

    int newId = dbManager.insertPelanggaran(bentukPelanggaran, kategori, poin);

    if (newId != -1) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.addRow(new Object[]{newId, bentukPelanggaran, kategori, poin});

        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField1.setText("");
    } else {
        JOptionPane.showMessageDialog(null, "Error adding new record");
    }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int selectedRow = jTable1.getSelectedRow(); 
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        jTextField2.setText(model.getValueAt(selectedRow, 0).toString());
        jTextField3.setText(model.getValueAt(selectedRow, 1).toString());
        jTextField4.setText(model.getValueAt(selectedRow, 2).toString());
        jTextField1.setText(model.getValueAt(selectedRow, 3).toString());
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
    if (selectedRow >= 0) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        // Get values from text fields
        int id = Integer.parseInt(jTextField2.getText());
        String bentukPelanggaran = jTextField3.getText();
        String kategori = jTextField4.getText();
        int poin = Integer.parseInt(jTextField1.getText());

        // Update the table model
        model.setValueAt(id, selectedRow, 0);
        model.setValueAt(bentukPelanggaran, selectedRow, 1);
        model.setValueAt(kategori, selectedRow, 2);
        model.setValueAt(poin, selectedRow, 3);

        // Update the database
        dbManager.updatePelanggaran(id, bentukPelanggaran, kategori, poin);

        // Clear input fields after updating (optional)
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField1.setText("");

        // Clear selection
        jTable1.clearSelection();

        JOptionPane.showMessageDialog(this, "Data berhasil diperbarui.", "Success", JOptionPane.INFORMATION_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(this, "Pilih baris yang ingin diperbarui.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton3ActionPerformed

